public class Overload {
    public int sum(int x, int y){
        return (x+y);
    }
    public int sum(int x , int y, int z){
        return (x+y+z);
    }
    public float sum(int x, float y){
        return (x+y);
    }
    public float sum(float x , float y){
        return (x+y);
    }
    public double sum(double x , float y){
        return (x+y);
    }
}
